            <p class="historyDesc">In today’s world of technology, everyone has becoming familiar with electronic commerce <br /> that enable users to book discovered event tickets as well as revolutionize event<br />  organizers’ marketing approach. However, to be exact, distributed commerce has been evolving for awhile which many technological expansions have made it all achievable.<br /> Inspired by Ticketmaster in the late 80s where people used to line up at the box office to buy tickets which then, the industry has moved to a physical distributed commerce platform for instance at record stores or venues. <br /><br />After that, ticketing shifted online through specific commerce platform and now we are in the era of new model of distributed commerce conducted online which in other words physical “expansion” back then in the late 80s but only that everything is online which in other word we are distributing event tickets across digital platforms. <br />In fact, with the mobile advancement we have, users are allowed to keep their virtual tickets on their phones while the organizers can sell tickets from their phones. <br /><br />At EventGaffer, we believe that staying ahead of mobile advancements means revolving the way products are built. Our vision is to provide a platform where anyone can easily build on, so that we can give our users the best experience possible. Essentially, we embrace partnerships that give our organizers the best tools out there for growing their businesses and that make it as easy as possible for attendees to buy tickets. When it comes to distributed commerce, this means helping organizers gain incremental ticket sales by keeping their customer right where they are from discovery through purchase.<br />Ten years from now, we hope that EventGaffer will be recognized as a trusted source which user found it both a safe and easy transaction system.</p>
